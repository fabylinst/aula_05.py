def caculadora(num1, num2,op):
    if op == 1:
        return  num2 + num1
    elif op == 2:
        # subtração
        return  num1 - num2
    elif op == 3:
    # multipicação
     return  num1 * num2
    elif op == 4:
    # divisão
      return  num1 // num2
    else:
        return'valor invalido' 